class Solution
{
    public boolean isBipartite(int V, ArrayList<ArrayList<Integer>>adj)
    {
        // Code here
        int[] color=new int[V];
        
        Arrays.fill(color, -1);
        for(int i=0; i<V; i++)
            if(color[i]==-1)
                if(!dfs(i, adj, color, 0))
                    return false;
        return true;
    }
    
    private boolean dfs(int i, ArrayList<ArrayList<Integer>>adj, int[] color, int parentColor){
        //changing the color to opposite of parent
        color[i]=1-parentColor;
        
        for(int j:adj.get(i))
            if(color[j]==-1){
                //doing dfs to color
                boolean res=dfs(j, adj, color, color[i]);
                
                if(!res)
                    return false;
            }
            else if(color[j]==color[i])
                return false;
        return true;
    }
}
