class Pair{
    Node node;
    int level;
    
    Pair(Node node, int level){
        this.node=node;
        this.level=level;
    }
}

class Solution{
    int res;
    int count;
    
    public int getCount(Node node, int bud)
    {
        //code here  
        Queue<Pair> aux=new LinkedList<>();
        int level=1, count=0;
        
        aux.add(new Pair(node, level));
        while(!aux.isEmpty()){
            Node root=aux.peek().node;
            level=aux.peek().level;
            
            if(root.left==null && root.right==null && bud>=level){
                count++;
                bud-=level;
            }
            aux.remove();
            if(root.left!=null)
                aux.add(new Pair(root.left, level+1));
            if(root.right!=null)
                aux.add(new Pair(root.right, level+1));
        }
        
        return count;
    }
}
