/*
The idea is that by reversing the linked list, we can start from the least significant digit and propagate the carry (if any) during the addition operation. If the carry still exists at the end of the list traversal, we add an additional node to represent the carried digit.
*/

class Solution {
    public Node addOne(Node head) {
        // code here.
        return reverse(add1(reverse(head)));
    }
    
    private Node add1(Node head){
        Node res=head;
        Node temp=null;
        //taking this as 1 since we have to add 1 to the linked list
        int carry=1, sum=0;
        
        while(head!=null){
            sum=carry+head.data;
            carry=sum>=10 ? 1 : 0;
            sum%=10;
            head.data=sum;
            temp=head;
            head=head.next;
        }
        if(carry>0)
            temp.next=new Node(carry);
        return res;
    }
    
        private Node reverse(Node head) {
        
        // Initialize three pointers: curr, prev and next
        Node curr = head, prev = null, next;

        // Traverse all the nodes of Linked List
        while (curr != null) {
            
            // Store next
            next = curr.next;
            
            // Reverse current node's next pointer
            curr.next = prev;
            
            // Move pointers one position ahead
            prev = curr;
            curr = next;
        }
        
        // Return the head of reversed linked list
        return prev;
    }
}

