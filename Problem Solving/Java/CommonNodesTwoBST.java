class Solution
{
    //Function to find the nodes that are common in both BST.
	public static ArrayList<Integer> findCommon(Node root1,Node root2)
    {
        //code here
        Set<Integer> aux=new HashSet<>();
        ArrayList<Integer> res=new ArrayList<>();
        
        toSet(root1, aux);
        toArrayList(root2, aux, res);
        return res;
    }
    
    private static void toSet(Node root, Set<Integer> aux){
        if(root==null)
            return;
        toSet(root.left, aux);
        aux.add(root.data);
        toSet(root.right, aux);
    }
    
    private static void toArrayList(Node root, Set<Integer> aux, ArrayList<Integer> res){
        if(root==null)
            return;
        toArrayList(root.left, aux, res);
        if(aux.contains(root.data))
            res.add(root.data);
        toArrayList(root.right, aux, res);
    }
}


