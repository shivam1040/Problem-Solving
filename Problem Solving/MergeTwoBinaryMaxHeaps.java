class Solution{
    public int[] mergeHeaps(int[] a, int[] b, int n, int m) {
        // your code here
        PriorityQueue<Integer> aux=new PriorityQueue<>(Collections.reverseOrder());
        int res[]=new int[m+n];
        int index=0;
        
        for(int i:a)
            aux.add(i);
        
        for(int i:b)
            aux.add(i);
            
        //System.out.println(aux);
        
        while(!aux.isEmpty()){
            int c=aux.poll();
            //System.out.println(c);
            res[index++]=c;
        }
        return res;
    }
}
