//User function Template for Java


class Compute 
{
    public long[] minAnd2ndMin(long a[], long n)  
    {
        long first=Long.MAX_VALUE, second=Long.MAX_VALUE;
        
        for(long i:a){
            if(i<first){
                second=first;
                first=i;
            }
            //ensures we dont put first in second if curr element is a duplicate of first
            else if(i!=first && i<second)
                second=i;
        }
        if(first==Long.MAX_VALUE || second==Long.MAX_VALUE)
            return new long[]{-1, -1};
        return new long[]{first, second};
    }
}

